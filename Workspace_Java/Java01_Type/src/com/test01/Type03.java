package com.test01;

public class Type03 {

	
	// 실수형 (소수점을 포함하는수) : float,double (실수형의 기본)
	public static void main(String[] args) {
	  float f1=0.1f;
	  float f2=9.9F;// f/F 생략 불가 
	  System.out.println(f1);
	  System.out.println(f2);
	  float sumF=f1+f2;
	  System.out.println(sumF+"="+f1+"+"+f2);
	  
	  System.out.println("------------");
	  
	  double d1=100.04;
	  double d2=9.06d; // 뒤에 d/D 생략가능  
	  double sumD=d1+d2;
	  System.out.println(sumD);
	  
	 
	 
		
	}
}
